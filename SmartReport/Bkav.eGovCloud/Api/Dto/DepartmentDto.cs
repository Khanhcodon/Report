using System.Runtime.Serialization;
namespace Bkav.eGovCloud.Api.Dto
{
    /// <summary>
    /// Phòng ban
    /// </summary>
    [DataContract]
    public class DepartmentDto
    {
        /// <summary>
        /// Lấy hoặc thiết lập id phòng ban
        /// </summary>
        [DataMember]
        public int DepartmentId { get; set; }

        /// <summary>
        /// Lấy hoặc thiết lập tên phòng ban
        /// </summary>
        [DataMember]
        public string DepartmentName { get; set; }

        /// <summary>
        /// Lấy hoặc thiết lập phòng ban cha
        /// </summary>
        [DataMember]
        public int? ParentId { get; set; }

        /// <summary>
        /// Lấy hoặc thiết lập id phòng ban mở rộng
        /// </summary>
        [DataMember]
        public string DepartmentIdExt { get; set; }

        /// <summary>
        /// Lấy hoặc thiết lập đường dẫn đầy đủ của tên phòng ban
        /// </summary>
        [DataMember]
        public string DepartmentPath { get; set; }

        /// <summary>
        /// Lấy hoặc thiết lập thứ tự sắp xếp
        /// </summary>
        [DataMember]
        public int Order { get; set; }

        /// <summary>
        /// Lấy hoặc thiết lập cấp độ phòng ban trên tree
        /// </summary>
        [DataMember]
        public int Level { get; set; }
    }
}
