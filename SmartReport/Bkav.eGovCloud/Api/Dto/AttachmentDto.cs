
using System;
namespace Bkav.eGovCloud.Api.Dto
{
    /// <summary>
    /// Thông tin file đính kem
    /// </summary>
    public class AttachmentDto
    {
        /// <summary>
        /// Tên file. Ví dụ: CongVan.pdf, FormDong.json
        /// </summary>
        public string Name { get; set; }

        /// <summary>
        /// Mô tả
        /// </summary>
        public string Description { get; set; }

        /// <summary>
        /// Giá trị đã được base64
        /// </summary>
        public string Value { get; set; }

        /// <summary>
        /// Lấy và thiết lập đường dẫn tải file đính kèm từ server liên thông
        /// </summary>
        public string Link { get; set; }
    }

    public class AttachmentBWSSDto
    {
        /// <summary>
        /// Tên file. Ví dụ: CongVan.pdf, FormDong.json
        /// </summary>
        public int AttachmentId { get; set; }

        /// <summary>
        /// Giá trị đã được base64
        /// </summary>
        public string AttachmentName { get; set; }

        /// <summary>
        /// Giá trị đã được base64
        /// </summary>
        public Guid DocumentId { get; set; }

        /// <summary>
        /// Giá trị đã được base64
        /// </summary>
        public bool IsDelete { get; set; }

        /// <summary>
        /// Kich
        /// </summary>
        public int Size { get; set; }
    }
}
