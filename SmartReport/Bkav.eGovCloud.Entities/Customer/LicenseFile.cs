using System;
namespace Bkav.eGovCloud.Entities.Customer
{
    /// <summary>
    /// Quản lý tệp đính kèm của giấy phép
    /// </summary>
    public class LicenseFile
    {
        /// <summary>
        /// Lấy hoặc thiết lập id của giấy phép
        /// </summary>
        public int BusinessLicenseId { get; set; }

        /// <summary>
        /// Lấy hoặc thiết lập Id của tệp đính kèm giấy phép
        /// </summary>
        public int BusinessLicenseAttackId { get; set; }

        /// <summary>
        /// Lấy hoặc thiết lập tên file
        /// </summary>
        public string FileLocationName { get; set; }

        /// <summary>
        /// Lấy hoặc thiết lập ngày tạo
        /// </summary>
        public DateTime DateCreated { get; set; }

        /// <summary>
        /// Lấy hoặc thiết lập nơi lưu trữ file
        /// </summary>
        public int FileLocationId { get; set; }

        /// <summary>
        /// Lấy hoặc thiết lập tên thư mục tự tăng chứa file
        /// </summary>
        public string IdentityFolder { get; set; }

        /// <summary>
        /// Lấy hoặc thiết lập tên thư mục gốc chứa file
        /// </summary>
        public string FileLocationKey { get; set; }
    }
}
