using Bkav.eGovCloud.Entities.Common;

namespace Bkav.eGovCloud.Entities.Customer
{
    /// <summary>
    /// Cấu hình có cho phép hiển thị node đăng ký qua mạng trên cây văn văn hay không
    /// </summary>
    public class OnlineRegistrationSettings : ISettings
    {
        /// <summary>
        /// Trạng thái kích hoạt
        /// </summary>
        public bool Active { get; set; }

        /// <summary>
        /// Lây hoặc thiết tên hiển thị
        /// </summary>
        public string Name { get; set; }

        /// <summary>
        /// Lây hoặc thiết lập api lấy dữ liệu
        /// </summary>
        public string ApiUrl { get; set; }

        /// <summary>
        /// Địa chỉ trang dịch vụ công trực tuyến
        /// </summary>
        public string OnlineLink { get; set; }

        /// <summary>
        /// Lấy hoặc thiết lập nhóm
        /// </summary>
        public int TreeGroupId { get; set; }

        /// <summary>
        /// Lấy hoặc thiết lập nhóm
        /// </summary>
        public int? PermissionSettingId { get; set; }

        /// <summary>
        /// Để tạm
        /// </summary>
        public string UserHasPermission { get; set; }
    }
}