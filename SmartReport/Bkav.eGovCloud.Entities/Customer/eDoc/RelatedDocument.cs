        /// Trích yếu chính xác của hồ sơ liên quan
        /// </summary>
        public string Subject { get; set; }

        /// <summary>
        /// Số hồ sơ liên quan
        /// </summary>
        public string CodeNumber { get; set; }

        /// <summary>
        /// Ký hiệu hồ sơ liên quan
        /// </summary>
        public string CodeNotation { get; set; }

        /// <summary>
        /// Ngày ban hành hồ sơ liên quan
        /// </summary>
        public string PromulgationDate { get; set; }
    }
}
