
using System;
namespace Bkav.eGovCloud.Entities.Customer.eDoc
{
    /// <summary>
    /// Class đại diện cho 1 tiến độ xử lý hồ sơ
    /// </summary>
    public class DocumentTrace
    {
        /// <summary>
        /// Lấy hoặc thiết lập id hồ sơ
        /// </summary>
        public int DocumentCopyId { get; set; }

        /// <summary>
        /// Lấy hoặc thiết lập số ký hiệu/ mã hồ sơ
        /// </summary>
        public string DocCode { get; set; }

        /// <summary>
        /// Lấy hoặc thiết lập cán bộ xử lý
        /// </summary>
        public string UserName { get; set; }

        /// <summary>
        /// Lấy hoặc thiết lập thời điểm xử lý
        /// </summary>
        public DateTime DateCreated { get; set; }

        /// <summary>
        /// Lấy hoặc thiết lập nội dung xử lý
        /// </summary>
        public string Comment { get; set; }

        /// <summary>
        /// Trạng thái hồ sơ
        /// </summary>
        public int Status { get; set; }

        /// <summary>
        /// Mã định danh cơ quan gửi
        /// </summary>
        public string OrganizationId { get; set; }
    }
}