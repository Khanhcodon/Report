using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;

namespace Bkav.eGovCloud.Entities.Customer
{
    /// <summary>
    /// Các yêu cầu bổ sung thường dùng
    /// </summary>
    public class RequiredSupplementary
    {
        /// <summary>
        /// Key
        /// </summary>
        public int RequiredSupplementaryId { get; set; }

        /// <summary>
        /// Lấy hoặc thiết lập nội dung yêu cầu bổ sung
        /// </summary>
        public string Name { get; set; }

        /// <summary>
        /// Lấy hoặc thiết lập loại hồ sơ sử dụng
        /// </summary>
        public Guid? DocTypeId { get; set; }

        /// <summary>
        /// Lấy hoặc thiết lập lĩnh vực sử dụng
        /// </summary>
        public int? DocFieldId { get; set; }

        /// <summary>
        /// Lấy hoặc thiết lập người sử dụng
        /// </summary>
        public int? UserId { get; set; }
    }
}
